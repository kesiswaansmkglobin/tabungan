export default function AuthenticatedLayout({ children }: PropsWithChildren) {
    const { auth, school } = usePage().props as any;
    const user = auth.user;
    const role = user?.primary_role || 'admin';
    const [sidebarOpen, setSidebarOpen] = useState(false);

    const filteredNav = navItems.filter((item) => item.roles.includes(role));

    const isActive = (href: string) => {
        if (href === '/dashboard') return route().current('dashboard');
        return window.location.pathname.startsWith(href);
    };

    return (
        <div className="flex h-screen overflow-hidden bg-gray-50 dark:bg-gray-900">
            {/* Sidebar */}
            <aside
                className={`fixed inset-y-0 left-0 z-30 flex w-64 flex-col bg-navy-500 transition-transform duration-200 ease-in-out dark:bg-navy-900 lg:static lg:translate-x-0 ${
                    sidebarOpen ? 'translate-x-0' : '-translate-x-full'
                }`}
            >
                <div className="flex h-16 shrink-0 items-center gap-2 border-b border-navy-400 px-6">
                    {school?.logo_url ? (
                        <img src={school.logo_url} alt="Logo" className="h-8 w-8 rounded-lg object-contain" loading="lazy" />
                    ) : (
                        <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-gold-500">
                            <svg className="h-5 w-5 text-navy-900" fill="none" viewBox="0 0 24 24" strokeWidth="2" stroke="currentColor">
                                <path strokeLinecap="round" strokeLinejoin="round" d="M12 6v12m-3-2.818l.879.659c1.171.879 3.07.879 4.242 0 1.172-.879 1.172-2.303 0-3.182C13.536 12.219 12.768 12 12 12c-.725 0-1.45-.22-2.003-.659-1.106-.879-1.106-2.303 0-3.182s2.9-.879 4.006 0l.415.33M21 12a9 9 0 11-18 0 9 9 0 0118 0z" />
                            </svg>
                        </div>
                    )}
                    <span className="text-lg font-semibold text-white">{school?.name || 'Tabungan Siswa'}</span>
                </div>

                <nav className="flex-1 space-y-1 overflow-y-auto px-3 pt-4 pb-4">
                    {filteredNav.map((item) => (
                        <Link
                            key={item.href}
                            href={item.href}
                            className={`sidebar-link ${
                                isActive(item.href)
                                    ? 'sidebar-link-active'
                                    : 'sidebar-link-inactive'
                            }`}
                            onClick={() => setSidebarOpen(false)}
                        >
                            {icons[item.icon]}
                            <span>{item.label}</span>
                        </Link>
                    ))}
                </nav>

                <div className="shrink-0 border-t border-navy-400 p-4">
                    <div className="text-xs text-navy-200">
                        SMK Globin &copy; {new Date().getFullYear()}
                    </div>
                </div>
            </aside>

            {/* Overlay */}
            {sidebarOpen && (
                <div
                    className="fixed inset-0 z-20 bg-black/50 lg:hidden"
                    onClick={() => setSidebarOpen(false)}
                />
            )}

            {/* Main Content */}
            <div className="flex flex-1 flex-col overflow-hidden">
                {/* Top Header */}
                <header className="flex h-16 shrink-0 items-center gap-4 border-b border-gray-200 bg-white px-4 dark:border-gray-700 dark:bg-navy-800">
                    <button
                        onClick={() => setSidebarOpen(true)}
                        className="text-gray-500 hover:text-gray-700 lg:hidden dark:text-gray-400"
                    >
                        <svg className="h-6 w-6" fill="none" viewBox="0 0 24 24" strokeWidth="1.5" stroke="currentColor">
                            <path strokeLinecap="round" strokeLinejoin="round" d="M3.75 6.75h16.5M3.75 12h16.5m-16.5 5.25h16.5" />
                        </svg>
                    </button>

                    <div className="flex flex-1 items-center justify-end gap-4">
                        <Dropdown>
                            <Dropdown.Trigger>
                                <span className="inline-flex rounded-md">
                                    <button
                                        type="button"
                                        className="inline-flex items-center gap-2 rounded-md px-3 py-2 text-sm font-medium text-gray-700 transition hover:text-gray-900 dark:text-gray-300 dark:hover:text-white"
                                    >
                                        <div className="flex h-8 w-8 items-center justify-center rounded-full bg-navy-500 text-xs font-semibold text-white">
                                            {user.name.charAt(0).toUpperCase()}
                                        </div>
                                        <span className="hidden sm:inline">{user.name}</span>
                                        <svg className="h-4 w-4" fill="none" viewBox="0 0 24 24" strokeWidth="2" stroke="currentColor">
                                            <path strokeLinecap="round" strokeLinejoin="round" d="M19.5 8.25l-7.5 7.5-7.5-7.5" />
                                        </svg>
                                    </button>
                                </span>
                            </Dropdown.Trigger>

                            <Dropdown.Content>
                                <Dropdown.Link href={route('profile.edit')}>
                                    Profile
                                </Dropdown.Link>
                                <Dropdown.Link href={route('logout')} method="post" as="button">
                                    Log Out
                                </Dropdown.Link>
                            </Dropdown.Content>
                        </Dropdown>
                    </div>
                </header>

                {/* Page Content */}
                <main className="flex-1 overflow-y-auto p-6">
                    {children}
                </main>
            </div>
        </div>
    );
}
